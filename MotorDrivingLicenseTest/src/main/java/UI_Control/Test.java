    public void OnOff(boolean a, boolean b, boolean c, boolean d) {
        this.aRButton.setSelected(a);
        this.bRButton.setSelected(b);
        this.cRButton.setSelected(c);
        this.dRButton.setSelected(d);
    }

    public void OnStatus(String n) {
        switch (n) {
            case "a":
                OnOff(true, false, false, false);
                break;
            case "b":
                OnOff(false, true, false, false);
                break;
            case "c":
                OnOff(false, false, true, false);
                break;
            case "d":
                OnOff(false, false, false, true);
                break;
            default:
                this.buttonGroup1.clearSelection();
                break;

        }
    }

